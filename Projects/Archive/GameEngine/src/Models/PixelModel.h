#ifndef PIXEL_H
#define PIXEL_H

#include "PositionModel.h"

///////////////////////////////////////////////////////////////////
/// @class PixelModel
///
/// Define's a pixel as a single character located at a position
///////////////////////////////////////////////////////////////////
class PixelModel
{
private:
   PositionModel m_position;
   char          m_character;

public:
   ///////////////////////////////////////////////////////////////////
   /// @fn Pixel
   ///////////////////////////////////////////////////////////////////
   PixelModel();

   ///////////////////////////////////////////////////////////////////
   /// @fn Pixel
   ///
   /// @param position
   /// @param character
   ///////////////////////////////////////////////////////////////////
   PixelModel(PositionModel position, char character);

   ///////////////////////////////////////////////////////////////////
   /// @fn setPosition
   ///
   /// @param position
   ///////////////////////////////////////////////////////////////////
   void setPosition(PositionModel position);

   ///////////////////////////////////////////////////////////////////
   /// @fn setCharacter
   ///
   /// @param character
   ///////////////////////////////////////////////////////////////////
   void setCharacter(char character);

   ///////////////////////////////////////////////////////////////////
   /// @fn getPosition
   ///////////////////////////////////////////////////////////////////
   PositionModel getPosition();

   ///////////////////////////////////////////////////////////////////
   /// @fn getCharacter
   ///////////////////////////////////////////////////////////////////
   char getCharacter();

   ///////////////////////////////////////////////////////////////////
   /// @fn Compare Functions
   ///////////////////////////////////////////////////////////////////
   int Compare(PixelModel pixel) const;

   bool operator==(PixelModel pixel) const { return 0 == Compare(pixel); }
   bool operator!=(PixelModel pixel) const { return 0 != Compare(pixel); }
};

#endif
