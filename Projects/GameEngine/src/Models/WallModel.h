#ifndef WALL_H
#define WALL_H

#include "PrintableModel.h"

///////////////////////////////////////////////////////////////////
/// @class WallModel
///
/// Acts as a printable object that can be toggled solid or visible
///////////////////////////////////////////////////////////////////
class WallModel : private PrintableModel
{
private:
public:
   ///////////////////////////////////////////////////////////////////
   /// @fn WallModel
   ///////////////////////////////////////////////////////////////////
   WallModel();

   ///////////////////////////////////////////////////////////////////
   /// @fn setVisible
   ///
   /// @param visible
   ///////////////////////////////////////////////////////////////////
   void setVisible(bool visible);

   ///////////////////////////////////////////////////////////////////
   /// @fn setSolid
   ///
   /// @param solid
   ///////////////////////////////////////////////////////////////////
   void setSolid(bool solid);

   ///////////////////////////////////////////////////////////////////
   /// @fn isSolid
   ///////////////////////////////////////////////////////////////////
   bool isSolid();

   ///////////////////////////////////////////////////////////////////
   /// @fn isVisible
   ///////////////////////////////////////////////////////////////////
   bool isVisible();
};

#endif
