#ifndef ENTITY_H
#define ENTITY_H

#include "PrintableModel.h"

///////////////////////////////////////////////////////////////////
/// @class EntityModel
///
/// defines an entity, or any object that can move
///////////////////////////////////////////////////////////////////
class EntityModel : public PrintableModel
{
private:
   bool m_isMovable;
   void (*m_movementFunction)(SpriteModel sprite);

public:
   ///////////////////////////////////////////////////////////////////
   /// @fn EntityModel
   ///////////////////////////////////////////////////////////////////
   EntityModel();

   ///////////////////////////////////////////////////////////////////
   /// @fn EntityModel
   ///
   /// @param sprite
   /// @param alias
   ///////////////////////////////////////////////////////////////////
   EntityModel(SpriteModel sprite, std::string alias, bool isMovable);

   ///////////////////////////////////////////////////////////////////
   /// @fn isMovable
   ///////////////////////////////////////////////////////////////////
   bool isMovable();

   ///////////////////////////////////////////////////////////////////
   /// @fn setMovementLogic
   ///
   /// @param movementFunction
   ///////////////////////////////////////////////////////////////////
   void setMovementLogic(void (*movementFunction)(SpriteModel sprite));

   ///////////////////////////////////////////////////////////////////
   /// @fn doMovement
   ///////////////////////////////////////////////////////////////////
   void doMovement();

   ///////////////////////////////////////////////////////////////////
   /// @fn setMoveable
   ///
   /// @param isMovable
   ///////////////////////////////////////////////////////////////////
   void setMoveable(bool isMovable);
};

#endif
