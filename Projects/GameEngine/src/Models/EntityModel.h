#ifndef ENTITY_H
#define ENTITY_H

#include "PrintableModel.h"

///////////////////////////////////////////////////////////////////
/// @class EntityModel
///
/// defines an entity, or any object that can move
///////////////////////////////////////////////////////////////////
class EntityModel : private PrintableModel
{
private:
   ///////////////////////////////////////////////////////////////////
   /// @fn immovable
   ///////////////////////////////////////////////////////////////////
   void immovable();

public:
   ///////////////////////////////////////////////////////////////////
   /// @fn EntityModel
   ///////////////////////////////////////////////////////////////////
   EntityModel();

   ///////////////////////////////////////////////////////////////////
   /// @fn isMovable
   ///////////////////////////////////////////////////////////////////
   bool isMovable();

   ///////////////////////////////////////////////////////////////////
   /// @fn setMovementLogic
   ///
   /// @param movementFunction
   ///////////////////////////////////////////////////////////////////
   void setMovementLogic(void (*movementFunction)());

   ///////////////////////////////////////////////////////////////////
   /// @fn setMoveable
   ///
   /// @param isMovable
   ///////////////////////////////////////////////////////////////////
   void setMoveable(bool isMovable);
};

#endif
