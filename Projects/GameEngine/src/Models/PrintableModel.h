#ifndef PRINTABLE_H
#define PRINTABLE_H

#include "SpriteModel.h"
#include <iostream>
#include <sstream>
#include <string>
#include <vector>

///////////////////////////////////////////////////////////////////
/// @class Printable
///
/// Designates an object as printable or not
///////////////////////////////////////////////////////////////////
class PrintableModel
{
private:
   std::string m_alias;
   SpriteModel m_sprite;

public:
   ///////////////////////////////////////////////////////////////////
   /// @fn PrintableModel
   ///////////////////////////////////////////////////////////////////
   PrintableModel();

   ///////////////////////////////////////////////////////////////////
   /// @fn setDimensions
   ///
   /// @param sprite
   /// @param alias
   ///////////////////////////////////////////////////////////////////
   PrintableModel(SpriteModel sprite, std::string alias);

   ///////////////////////////////////////////////////////////////////
   /// @fn getAlias
   ///////////////////////////////////////////////////////////////////
   std::string getAlias();

   ///////////////////////////////////////////////////////////////////
   /// @fn getSprite
   ///////////////////////////////////////////////////////////////////
   SpriteModel getSprite();

   ///////////////////////////////////////////////////////////////////
   /// @fn setAlias
   ///////////////////////////////////////////////////////////////////
   void setAlias(std::string alias);

   ///////////////////////////////////////////////////////////////////
   /// @fn setSprite
   ///////////////////////////////////////////////////////////////////
   void setSprite(SpriteModel sprite);

   ///////////////////////////////////////////////////////////////////
   /// @fn Compare Functions
   ///////////////////////////////////////////////////////////////////
   int Compare(PrintableModel printable) const;

   bool operator==(PrintableModel printable) const { return 0 == Compare(printable); }
};

#endif
