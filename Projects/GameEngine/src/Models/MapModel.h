#ifndef MAP_H
#define MAP_H

#include "EntityModel.h"
#include "PrintableModel.h"
#include "WallModel.h"

///////////////////////////////////////////////////////////////////
/// @class MapModel
///
/// defines a map and the walls and entities within
///////////////////////////////////////////////////////////////////
class MapModel : private PrintableModel
{
private:
public:
   ///////////////////////////////////////////////////////////////////
   /// @fn MapModel
   ///////////////////////////////////////////////////////////////////
   MapModel();

   ///////////////////////////////////////////////////////////////////
   /// @fn MapModel
   ///
   /// @param length
   /// @param width
   /// @param alias
   ///////////////////////////////////////////////////////////////////
   MapModel(int length, int height, std::string alias);

   ///////////////////////////////////////////////////////////////////
   /// @fn getWallAtPosition
   ///
   /// @param position
   ///////////////////////////////////////////////////////////////////
   WallModel getWallAtPosition(PositionModel position);

   ///////////////////////////////////////////////////////////////////
   /// @fn getEntityAtPosition
   ///
   /// @param position
   ///////////////////////////////////////////////////////////////////
   EntityModel getEntityAtPosition(PositionModel position);

   ///////////////////////////////////////////////////////////////////
   /// @fn getWallWithAlias
   ///
   /// @param alias
   ///////////////////////////////////////////////////////////////////
   WallModel getWallWithAlias(std::string alias);

   ///////////////////////////////////////////////////////////////////
   /// @fn getEntityWithAlias
   ///
   /// @param alias
   ///////////////////////////////////////////////////////////////////
   EntityModel getEntityWithAlias(std::string alias);

   ///////////////////////////////////////////////////////////////////
   /// @fn setWallAtPosition
   ///
   /// @param wall
   ///////////////////////////////////////////////////////////////////
   void addWall(WallModel wall);

   ///////////////////////////////////////////////////////////////////
   /// @fn setEntityAtPosition
   ///
   /// @param entity
   ///////////////////////////////////////////////////////////////////
   void addEntity(EntityModel entity);
};

#endif
