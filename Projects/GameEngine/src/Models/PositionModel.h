#ifndef POSITION_H
#define POSITION_H

///////////////////////////////////////////////////////////////////
/// @class Position
///
/// Define's the center point for a printable object
///////////////////////////////////////////////////////////////////
class PositionModel
{
private:
   int m_x;
   int m_y;

public:
   ///////////////////////////////////////////////////////////////////
   /// @fn PositionModel
   ///////////////////////////////////////////////////////////////////
   PositionModel();

   ///////////////////////////////////////////////////////////////////
   /// @fn PositionModel
   ///
   /// @param x
   /// @param y
   ///////////////////////////////////////////////////////////////////
   PositionModel(int x, int y);

   ///////////////////////////////////////////////////////////////////
   /// @fn setPosition
   ///
   /// @param x
   /// @param y
   ///////////////////////////////////////////////////////////////////
   void setLocation(int x, int y);

   ///////////////////////////////////////////////////////////////////
   /// @fn getX
   ///////////////////////////////////////////////////////////////////
   int getX();

   ///////////////////////////////////////////////////////////////////
   /// @fn getY
   ///////////////////////////////////////////////////////////////////
   int getY();

   ///////////////////////////////////////////////////////////////////
   /// @fn Compare Functions
   ///////////////////////////////////////////////////////////////////
   int Compare(PositionModel position) const;

   bool operator==(PositionModel position) const { return 0 == Compare(position); }
   bool operator>(PositionModel position) const { return Compare(position) > 0; }
   bool operator<(PositionModel position) const { return Compare(position) < 0; }
};

#endif
