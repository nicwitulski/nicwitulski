#ifndef ENTITY_H
#define ENTITY_H

#include "PrintableModel.h"

///////////////////////////////////////////////////////////////////
/// @class EntityModel
///
/// defines an entity, or any object that can move
///////////////////////////////////////////////////////////////////
class EntityModel : public PrintableModel
{
private:
   bool m_isMovable;

public:
   ///////////////////////////////////////////////////////////////////
   /// @fn EntityModel
   ///////////////////////////////////////////////////////////////////
   EntityModel();

   ///////////////////////////////////////////////////////////////////
   /// @fn EntityModel
   ///
   /// @param sprite
   /// @param alias
   ///////////////////////////////////////////////////////////////////
   EntityModel(SpriteModel sprite, std::string alias, bool isMovable);

   ///////////////////////////////////////////////////////////////////
   /// @fn isMovable
   ///////////////////////////////////////////////////////////////////
   bool isMovable();

   ///////////////////////////////////////////////////////////////////
   /// @fn setMoveable
   ///
   /// @param isMovable
   ///////////////////////////////////////////////////////////////////
   void setMovable(bool isMovable);
};

#endif
